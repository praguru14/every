package com.epps.framework.infrastructure.model.entities;

import javax.persistence.Column;

public class EppsRoleCodeBaseEntity extends EppsCreatorBaseEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8301376230240941826L;

	private Integer creatorRoleCode;
	
	private Integer modifierRoleCode;

	
	@Column(name="creator_role_cd")
	public Integer getCreatorRoleCode() {
		return creatorRoleCode;
	}

	public void setCreatorRoleCode(Integer creatorRoleCode) {
		this.creatorRoleCode = creatorRoleCode;
	}

	@Column(name="modifier_role_cd")
	public Integer getModifierRoleCode() {
		return modifierRoleCode;
	}

	public void setModifierRoleCode(Integer modifierRoleCode) {
		this.modifierRoleCode = modifierRoleCode;
	}

}
